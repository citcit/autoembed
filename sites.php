/* Explanation of items
	Mandatory ones must be specified, however if it defaults to something, it won't cause a problem if its left off. Optional ones aren't needed.
	All [regex] patterns are only partial. So don't specify delimiters etc.  The regex is completed when its used.

	array(
		'id' =>					Unique 3 chars comprising A-Z0-9 (must not be any other item with the same ID) eg YTB for YouTube	(Mandatory)
		'title' =>				Title of the Website - shown in the Admin cp for each site as hover link title					(Mandatory)
		'added' =>				Version of this mod when this site was first supported all pre v4 are <4.0 then subsequent 4.0, 1.1 etc (Optional)
		'website' =>			Website url (to main site) - used in Admin Cp only										(Mandatory)
		'type' => 'video',		Type of site, must be either video|audio|other										(Mandatory - but defaults to video)
		'plugin' => 'flash',	Type of plugin used by the site either flash|divx|wmp									(Mandatory - but defaults to flash)
		'embed-enabled' =>		The default setting for whether this site is enabled (no need to edit this, use Admin Cp instead)		(Optional - but defaults to true)
		'embed-pattern' =>		[Regex] Match these urls for embedding											(Mandatory)
		'embed-movie' =>			The FINAL embed url, (any flashVars should be added as querystring)						(Mandatory)
								$1 = FULL raw link
								$2, $3... etc are any patterns you specify for parts 
								Note, &ampersands don't need to be encoded. All ampersands in final 'object' are made into &amp;
		'embed-hq-movie' =>		Primarily for youtube. A different link which gives higher quality (but same sizes)				(Optional)
		'embed-width' =>		Width of the embedded object	(don't specify any units, as px is specified later)					(Mandatory)
		'embed-height' =>			Height of the embedded object	(don't specify any units, as px is specified later)					(Mandatory)
		'embed-html-before' =>		Html to appear before the embedded object											(Optional)
		'embed-html-after' =>		Html to appear after the embedded object											(Optional)
		'embed-nolink' =>			Boolean value to prevent including the raw link in the post. (For sites where there is a link in the player)	(Optional - but defaults to false)
		'fix-html-pattern' =>		[Regex] Noobs users might post the 'embed' html rather than a link, so on posting, replace with link		(Optional)
								$1 = gets replaced by the embed pattern
		'fix-html-url' =>		Url for the fixed url to point to a page rather than the embed url							(Optional)
		'lookup-enabled' =>		Disable lookups for this site (unused, but available for use since you can't change this via admin		(Optional - but defaults to true)
		'lookup-url' =>			[Regex] On posting match these urls as we need to do a lookup to grab an embeddable url			(Mandatory to do Lookups)
		'lookup-url-urldecode' =>	Url decode the url just before doing a lookup. (Primarily for Archive.org)						(Optional, but defaults to false)
		'lookup-actual-url' =>		Lookup couldn't be done to the url above, so using the variables from above, lookup here			(Optional)
		'lookup-pattern' =>		[Regex] Match this pattern to obtain the bit that we want								(Mandatory to do lookups)
		'lookup-final-url' =>	Url, if the above pattern only returns part of the url, using the variables collected we complete the url	(Optional)
								$1 = gets replaced by the first pattern from lookup-pattern
		'lookup-check' =>		Checks whether input[3] in lookup url is found in final url (was necessary for bbc news) as they don't
								differentiate between video only pages, and news pages with video.							(Optional, but defaults to false)
		'lookup-unencode' =>	Set true or 1 if you want html entities to be reversed in the url								(Optional)
		'lookup-urldecode' =>	Number of times to run urldecode on the final url.  Normally 1, but there is 1 site requiring 2			(Optional)
		// Example urls			I include example urls with EVERY site to show the format supported and to make sure that they work
							for future compatibility.
	),
*/

